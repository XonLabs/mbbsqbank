import { JSX, SVGProps } from 'react';
import {
  fetchTopicStatistics,
  fetchTopicTotalQuestionCount,
  fetchTopicsList,
} from '../actions';
import loadAppData from '~/lib/server/loaders/load-app-data';
import TopicTable from '../components/TopicTable';

export default async function Component() {
  const appData = await loadAppData();

  if (!appData.auth.user.email?.endsWith('@connect.hku.hk')) {
    return (
      <>
        <div className="text-center text-2xl font-bold mt-10">
          Access Restricted
        </div>
        <div className="text-center text-xl mt-5">
          Your email address must end with @connect.hku.hk to access this page.
        </div>
      </>
    );
  }
  const userID = appData.user.id;
  const topic_list = await fetchTopicsList(userID);

  const stats = await fetchTopicStatistics(userID);

  const topicCounts = await fetchTopicTotalQuestionCount();

  console.log('stats', stats);

  return (
    <>
      <TopicTable
        topic_list={topic_list}
        topic_stats={stats}
        topic_counts={topicCounts}
      />
    </>
  );
}

